// Helper functions for working with bonuses
export const getExclusiveBonuses = () => {
  return bonuses.filter((bonus) => bonus.isExclusive);
};

export const getBonusesByType = (type) => {
  return bonuses.filter((bonus) => bonus.types.includes(type));
};

export const getBonusesByMinDeposit = (minAmount) => {
  return bonuses.filter((bonus) => bonus.minDeposit <= minAmount);
};

export const getBonusesByMaxWagering = (maxWager) => {
  return bonuses.filter((bonus) => bonus.wagering <= maxWager);
};

export const getFeaturedBonus = () => {
  return bonuses.find((bonus) => bonus.featured) || bonuses[0];
};

export const getTopBonuses = (limit = 5) => {
  return [...bonuses].sort((a, b) => b.rating - a.rating).slice(0, limit);
};

export const getNewBonuses = (limit = 5) => {
  return bonuses.filter((bonus) => bonus.isNew).slice(0, limit);
};
